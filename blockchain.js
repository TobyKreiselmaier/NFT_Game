const marketplace = new web3.eth.Contract(marketplaceAbi, "0xaDfc41eF5B92F6ab0DD231B73c8625F2d2Bf9054");

let numberOfPotions, numberOfBoots, numberOfCapes;

function getUserItems(callback) {
  web3.eth.getAccounts().then(accounts => {

    const tokenPromise1 = token.methods.balanceOf(accounts[0], 1).call();
    const tokenPromise2 = token.methods.balanceOf(accounts[0], 2).call();
    const tokenPromise3 = token.methods.balanceOf(accounts[0], 3).call();

    Promise.all([tokenPromise1, tokenPromise2, tokenPromise3]).then(values => {
      if(values[0] > 0) {
        numberOfPotions = values[0];
        numberOfBoots = values[1];
        numberOfCapes = values[2];
      }
      if(numberOfPotions > 0)
        COIN_GENERATION_INTERVAL = COIN_GENERATION_INTERVAL * Math.pow(0.75, numberOfPotions);
      if(numberOfBoots > 0)
        PLAYER_SPEED = PLAYER_SPEED * Math.pow(1.3, numberOfBoots);
      if(numberOfCapes > 0)
        GAME_SECOND = GAME_SECOND * Math.pow(1.5, numberOfCapes);
      callback();
    })
  })
}

function buy(id) {
    web3.eth.getAccounts().then(accounts => {
      var options = {
        from: accounts[0],
        value: 0
      };
      if(id == 1)
        options.value = 100000000000000; // 1/10,000 NRG
      else if(id == 2)
        options.value = 200000000000000; // 2/10,000 NRG
      else if(id == 3)
        options.value = 300000000000000; // 3/10,000 NRG

      marketplace.methods.buyToken(id).send(options)
      .on('receipt', () => {
          alert('Transaction completed successfully');
      });
    })
};
